#Soru 6: Bu sorudaki göreviniz,kullanıcının klavye ile gireceği bir sayıya kadar olan
#tüm çift sayıların toplamını hesaplayacak bir Python programı yazmak olacaktır.

toplam = 0
x = int(input("Bir sayı giriniz: "))
for i in range(0,(x+1),2):
    toplam += i
print(f"Girmiş olduğunuz {x} sayısına kadar olan çift sayılar toplamı: ", toplam)

