# Soru 5: Faktöriyel: dışardan girilen bir sayının faktöriyelini hesaplayınız.
x = int(input("Bir sayı giriniz: "))
sonuc = 1
while x > 0 :
    sonuc = sonuc * x
    x -= 1
print("Girmiş olduğunuz sayının faktöriyeli: ", sonuc)

